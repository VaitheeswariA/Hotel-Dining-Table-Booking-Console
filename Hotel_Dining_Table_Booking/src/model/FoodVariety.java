package model;

public class FoodVariety {
	private int variety_Id;
	private String variety_type;
	
	
	
	public int getVariety_Id() {
		return variety_Id;
	}
	public void setVariety_Id(int variety_Id) {
		this.variety_Id = variety_Id;
	}
	public String getVariety_type() {
		return variety_type;
	}
	public void setVariety_type(String variety_type) {
		this.variety_type = variety_type;
	}
	
	
}
